package com.tns.operator;

public class If_else {

	public static void main(String[] args)
	              //cascade if else....
	{
		int x=10;
		if(x == 10)
		{
			System.out.println("X equals to 10");
		}
		else if(x > 10)
		{
			System.out.println("X is greater to 10");		
		}
		else 
		{
			System.out.println("X is smaller than 10");
		}
		
	}

}

